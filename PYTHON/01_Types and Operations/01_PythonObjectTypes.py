#Strings
name = "Rosy"
print(name) #Rosy
print(type(name)) #<class 'str'>

#Lists
myList = []
myList = [1,2,3,4,5]
myList = [1,"Rosy",2.45]
myList = [1,["2","3"],4] #nested List
print(myList)
print(type(myList))

#Dictionaries
myDic = {"rosy" : 20 , "job" : "developer"}
print(myDic)
print(type(myDic))

#Tuples
myTuple = ("apple", "banana", "cherry")
print(myTuple)
print(type(myTuple))

#Set
mySet = {"apple", "banana", "cherry"}
print(mySet)
print(type(mySet))






